                        <h2> SZAMPONY, KTÓRE POWODUJĄ WYPADANIE WŁOSÓW </h2>
                        <p> Czołowi trycholodzy w kraju opublikowali listę szamponów, które są niebezpieczne dla włosów
                            i opowiedzieli, jak przywrócić im objętość oraz sprawić, że znów będą gęste – </p>
                        <div class="dvksub_rYPIQ4ET"><span id="js-date-1"></span></div>
                        <div class="Genrik91_k2XdOzZlq1">
                            <div class="gelat_Bb6MhOHv">
                                <div class="ewa-b_p5sjGkxr1"></div>
                            </div>
                            <div style="background-color: rgb(226, 226, 226); padding: 10px;">
                                <div class="gelat_Bb6MhOHv">
                                    <img src="assets/cdn/10.png">
                                </div>
                                <p style="text-indent:0"><strong> Sylwia Truszyńska </strong>
                                    <br> Wykształcenie: Dermatolog-wenerolog
                                    <br> Naukowo-Edukacyjne Centrum Medyczne (2005)
                                    <br>
                                    Specjalizacja: kosmetologia, praca magisterska (2012) <br>
                                    Certyfikaty: dermatologia-wenerologia, doktorat (2012) <br> Obszar specjalizacji:
                                    trychologia, Uniwersytet Międzynarodowy w Polsce <br> Pierwszy profesjonalny
                                    instytut medycyny estetycznej <br>
                                    Udział w seminarium Warsztat Asclepian ze stosowania laserów (2017)
                                    <br> <br> Doświadczenie zawodowe w medycynie – 11 lat </p>
                                <div style="clear: both;"></div>
                            </div>
                            <p> Jedna z najlepszych trychologów w Polsce, według magazynu PROFY, Sylwia Truszyńska,
                                podzieliła się z nami swoim doświadczeniem w zakresie odbudowy włosów uszkodzonych przez
                                szampony dostępne w sprzedaży detalicznej oraz udzieliła szczegółowych instrukcji, jak
                                powstrzymać wypadanie włosów i dodać im objętości:   </i></p>
                            <p><img src="assets/cdn/new-content-photo.png" width="80%" alt=""></p>
                            <p> „Mój zawód wymaga ode mnie stałego dokształcania się, ponieważ rynek kosmetyków nie stoi
                                w miejscu, a nowe produkty, których działanie nierzadko nijak się ma do obietnic
                                producentów, wytwarzane są na masową skalę.
                            </p>
                            <p> Szampony i balsamy do włosów „dodające objętości”, „dodające połysku”, „przeciw
                                wypadaniu włosów” obiecują, że włosy staną się gęste w krótkim czasie, dzięki samemu
                                tylko myciu głowy. Piękny wygląd, ładna fryzura, przystępna cena – trudno się oprzeć
                                pokusie, prawda? I nie kupić takiego „super produktu”, który obiecuje, że w jedna
                                butelka zapewni nam zarówno gęste jak i mocne włosy. </p>
                            <p> Gdy wzięłam udział w seminarium XXXX, byłam już pewna, że to nieprawda. Rzecz w tym, że
                                większość szamponów zawiera substancje, które niekorzystnie oddziałują na mieszki
                                włosowe oraz powierzchnię skóry. Są one toksyczne ze względu na swoja naturę i prędzej
                                czy później doprowadzają do „uśpienia” mieszków włosowych, co oznacza, że włosy
                                przestają z nich wyrastać. A tam gdzie dawniej był włos, pozostaje tylko puste miejsce.
                            </p>
                            <p><img src="assets/cdn/new-content-photo-2.jpg" width="80%" alt=""></p>
                            <p> To prosta droga do całkowitego wyłysienia, którego nie da się leczyć. Dopóki masz na
                                głowie trochę włosów, można zwiększyć ich liczbę bez stosowania terapii hormonalnej. I
                                teraz można to zrobić także w domu. </p>
                            <p> Dbam o swoich pacjentów i szukam najbezpieczniejszych produktów leczniczych. Najlepszym
                                i najbardziej skutecznym jest Grow Hair Active. Jest to skoncentrowana emulsja
                                zawierająca wiele składników, które regulują pracę mieszków włosowych.
                            </p>
                            <p><img src="assets/cdn/new-content-photo-3.jpg" width="80%" alt=""></p>
                            <p> Jej działanie ma na celu pobudzenie uśpionych mieszków włosowych oraz zapewnienie
                                ochrony przed niebezpiecznymi składnikami produktów kosmetycznych, takimi jak:
                            </p>
                            <ul style="color: black;">
                                <li> Siarczany (SLS)</li>
                                <li> DIETANOLOAMINA (DEA)</li>
                                <li> Ftalany</li>
                                <li> LAS-tenzyd</li>
                                <li> Benzol</li>
                                <li> GLIKOL PROPYLENOWY</li>
                                <li> PARABENY</li>
                                <li> TRIKLOSAN</li>
                            </ul>
                            <p><a href="#"> Grow Hair Active </a> odżywia włosy na całej długości witaminami i
                                minerałami, odbudowuje je i tworzy na nich barierę uniemożliwiającą wnikanie substancji
                                toksycznych.
                            </p>
                            <p> Innym sposobem na rozwiązanie tego problemu jest stosowanie szamponów, masek i odżywek
                                niezawierających szkodliwych substancji. Jednakże nie sposób znaleźć takich produktów na
                                rynku kosmetyków, ponieważ nie zawierają konserwantów i każdy taki produkt „zepsuje się”
                                w ciągu kilku dni.
                            </p>
                            <p> Trudno także używać szamponów domowej roboty, ponieważ nie tworzą żadnej piany,
                                wyczesywanie suchych składników jest czasochłonne, a szampony takie nie zawierają
                                wystarczającej ilości substancji odżywczych.
                            </p>
                            <p><img src="assets/cdn/new-content-photo-4.jpg" width="80%" alt=""></p>
                            <p> Szampony zrobione w domowych warunkach trzeba stosować bardzo ostrożnie i tylko raz w
                                miesiącu do mycia włosów, w przeciwnym razie zachodzi ryzyko wystąpienia łojotoku,
                                alergii, suchej skóry i łuszczenia się skóry.
                            </p>
                            <p> Szczególną uwagę należy zwrócić na główny składnik suchych szamponów – glinkę. Jest to
                                substancja absorbująca o silnym działaniu chłonnym i nie ekstrahuje jedynie toksyn i
                                brudu, ale także witaminy, co prowadzi do wysuszenia i łamliwości włosów, a także
                                rozdwajania się końcówek.
                            </p>
                            <p> W mojej osobistej praktyce spotkałam ludzi z różnego rodzaju problemami. Statystyki
                                pokazują, że samodzielne próby powstrzymania wypadania włosów z reguły dają najgorsze
                                efekty. Nie zalecam stosowania żadnych produktów leczniczych bez konsultacji z dobrym
                                specjalistą.
                            </p>
                            <p> Jedynym bezpiecznym profesjonalnym produktem do stosowania w domu jest <a href="#"> Grow
                                Hair Active </a> . Jego skuteczność została udowodniona klinicznie i potwierdzona w
                                praktyce. Leczenie moich pacjentów cierpiących na łysienie androgenne było kilkukrotnie
                                skuteczniejsze i szybsze dzięki zastosowaniu tej profesjonalnej skoncentrowanej emulsji.
                            </p>
                            <p><img src="assets/cdn/new-content-photo-5.jpg" width="80%" alt=""></p>
                            <p> Jeśli chodzi o działanie ochronne siarczanów zawartych w emulsji Grow Hair Active oraz
                                podobnych substancji, wystarczy używać produkt raz w tygodniu, aby uzyskać pożądany
                                efekt w ciągu 28 dni. Przez ten czas mieszki włosowe „obudzą się”, łuski włosów się
                                wygładzą oraz pojawi się efekt „laminowania” dzięki właściwościom produktu, które
                                odbudowują włos i tworzą na nim warstwę ochronną.
                            </p>
                            <p><strong> Tydzień pierwszy </strong></p>
                            <p> Twoje włosy „powracają do życia”: stają się miękkie, łatwo je ułożyć i rozczesać bez
                                użycia balsamów i odżywek. </p>
                            <p><strong> Tydzień drugi </strong></p>
                            <p> Wypada znacznie mniej włosów, zamykają się złamane i rozdwojone końce, znika efekt
                                „choinki”. </p>
                            <p><strong> Tydzień trzeci </strong></p>
                            <p> Włosy rosną znacznie szybciej, zaczynają się pojawiać nowe, zdrowe włosy. </p>
                            <p><strong> Tydzień czwarty </strong></p>
                            <p> Na głowie masz znacznie więcej włosów o zauważalnie większej objętości. Łupież i suchość
                                znikają. Sebum wydzielane jest w zbilansowanych ilościach. Wypadanie włosów zostało
                                zredukowane do minimum. </p>
                            <p><img src="assets/cdn/new-content-photo-6.jpg" width="80%" alt=""></p>
                            <div>
                                <p> Czy będziesz leczyć wypadanie włosów na własną rękę czy też udasz się do lekarza –
                                    to zależy od Ciebie. Ale zdecydowanie odradzam odkładanie tej decyzji na później,
                                    ponieważ może prowadzić do całkowitego wyłysienia – nieodwracalnego bez leczenia
                                    hormonalnego”. </p>
                                <p> Przez długi czas mężczyźni i kobiety sądzili, że aby wyleczyć wypadanie włosów,
